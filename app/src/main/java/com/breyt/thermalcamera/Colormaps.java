    /**
     * Applies a colormap to a grayscale value.
     *
     * @param colormap The colormap LUT
     * @param value Grayscale value (0-255)
     * @return ARGB color value
     */
    public static int apply(int[][] colormap, int value) {
        int idx = Math.max(0, Math.min(255, value));
        int[] rgb = colormap[idx];
        return 0xFF000000 | (rgb[0] << 16) | (rgb[1] << 8) | rgb[2];
    }

    /**
     * Applies a colormap to a normalized value.
     *
     * @param colormap The colormap LUT
     * @param value Normalized value (0.0 - 1.0)
     * @return ARGB color value
     */
    public static int applyNormalized(int[][] colormap, float value) {
        int idx = (int) (Math.max(0f, Math.min(1f, value)) * 255f);
        int[] rgb = colormap[idx];
        return 0xFF000000 | (rgb[0] << 16) | (rgb[1] << 8) | rgb[2];
    }
